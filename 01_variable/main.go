package main

import "fmt"

func main() {
	// mendeklarasikan nilai variabel
	// var name string = "Danil syah"

	// variabel tanpa deklarasi
	// string , default value = null
	var name string
	name = "haykal"

	// integer , default value = 0
	var umur int = 25
	age := 20
	age = 26
	var jumlah int

	// bool
	var belajarGolang bool = true

	// float , pecahan
	var rumus float32
	rumus = 3.14

	fmt.Println(name)
	fmt.Println(umur)
	fmt.Println(belajarGolang)
	fmt.Println(age)
	fmt.Println(jumlah)
	fmt.Println(rumus)
}